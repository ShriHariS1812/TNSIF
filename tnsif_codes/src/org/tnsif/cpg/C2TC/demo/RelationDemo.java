package org.tnsif.cpg.C2TC.demo;

public class RelationDemo {
		public static void main(String[] args) {
			
			int a=10 ,b=5;
			
			System.out.println("a==b : " + (a==b)); //false
			System.out.println("a!=b : " + (a!=b)); //true
			System.out.println("a>b  :  "+ (a>b)); //true
			System.out.println("a<b  :  "+ (a<b)); //false
			System.out.println("a>=b :  "+ (a>=b)); //true
			System.out.println("a<=b  : "+ (a<=b)); //false

		}
}
